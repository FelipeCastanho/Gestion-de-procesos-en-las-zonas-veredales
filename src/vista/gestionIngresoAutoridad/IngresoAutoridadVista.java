    private void jButtonRegistrarIngresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarIngresoActionPerformed
        try {
            Zonaveredal zonaVeredal = zonaveredalLogica.buscarZonaVeredalPorNombre((String)jComboBoxZonasVeredales.getSelectedItem());
            Autoridad autoridad = autoridadLogica.buscarAutoridad(Integer.parseInt(jTextFieldIdentificarAutoridad.getText()));
            if(autoridad == null){
                autoridad = new Autoridad();
                autoridad.setNombreAutoridad(jTextFieldNombreAutoridad.getText());
                autoridad.setIdAutoridad(Integer.parseInt(jTextFieldIdentificarAutoridad.getText()));
                autoridadLogica.registrarAutoridad(autoridad);    
            }
            Autoridadingresazonaveredal ingreso = new Autoridadingresazonaveredal();
            ingreso.setIdAutoridad(autoridad);
            ingreso.setIdZonaVeredal(zonaVeredal);
            ingreso.setIdIngreso(ingresoLogica.buscarSiguienteId());
            ingreso.setMotivoIngreso(jTextAreaMotivoIngreso.getText());
            autoridad.getNombreAutoridad().charAt(1);
            jTextAreaMotivoIngreso.getText().charAt(1);
            ingresoLogica.registrarIngresoAutoridad(ingreso);
            JOptionPane.showMessageDialog(ventana, "Se ha registrado el ingreso satisfactoriamente");
            jTextAreaMotivoIngreso.setText("");
            jTextFieldIdentificarAutoridad.setText("");
            jTextFieldNombreAutoridad.setText("");
            Calendar calendar = Calendar.getInstance();
            Date fecha = calendar.getTime();
            SimpleDateFormat formatoDeFecha = new SimpleDateFormat("dd/MM/yyyy");
            jTextFieldFechaIngreso.setText(formatoDeFecha.format(fecha));
            
        }catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(ventana, "El identificador de autoridad ingresado es incorrecto");
        }catch (StringIndexOutOfBoundsException ex){
            JOptionPane.showMessageDialog(ventana, "Ningun campo debe estar vacio o incompleto");
        }catch (Exception ex) {
            JOptionPane.showMessageDialog(ventana, ex.getMessage());
        } 
       
    }//GEN-LAST:event_jButtonRegistrarIngresoActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Date fechaIngreso = jDateChooserIngreso.getDate();
        Date fechaSalida = jDateChooserSalida.getDate();
        List<Autoridadingresazonaveredal> ingresos;
        if(fechaIngreso != null && fechaSalida != null){
            ingresos = ingresoLogica.buscarPorFecha(fechaIngreso, fechaSalida);
            String datosAMostrar[] = crearListaDesplegable(ingresos);
            String opcion = (String) JOptionPane.showInputDialog(ventana,"Ingresos registrados en ese intervalo:", "Elegir uno para ver detalles",JOptionPane.QUESTION_MESSAGE,
                        null,datosAMostrar, datosAMostrar[0]);
            int seleccion = obtenerSeleccion(datosAMostrar, opcion);
            if(seleccion == -1) JOptionPane.showMessageDialog(ventana, "No se ha seleccionado nada para mostrar");
            else{
                Autoridadingresazonaveredal detalle = ingresos.get(seleccion);
                jComboBoxZonasVeredales.setSelectedIndex(detalle.getIdZonaVeredal().getIdZonaVeredal());
                jComboBoxZonasVeredales.setSelectedItem(jComboBoxZonasVeredales.getItemAt(detalle.getIdZonaVeredal().getIdZonaVeredal()));
            }

        }
        else{
            JOptionPane.showMessageDialog(ventana, "Debe seleccionar un rango de fechas");
        }
       
    }//GEN-LAST:event_jButton3ActionPerformed

    public int obtenerSeleccion(String ingresos[], String opcion){
         for (int i = 0; i < ingresos.length; i++) {
            if(ingresos[i].equals(opcion)) return i;
        }
        return -1;
    }
    
    public String[] crearListaDesplegable(List<Autoridadingresazonaveredal> ingresos){
        String respuesta[] = new String[ingresos.size()];
        for (int i = 0; i < ingresos.size(); i++) {
            respuesta[i] = "Zona veredal: "+ingresos.get(i).getIdZonaVeredal().getNombreZonaVeredal() + "          Autoridad: " + ingresos.get(i).getIdAutoridad().getNombreAutoridad()
                    +"          Fecha de entrada: "+ ingresos.get(i).getFechaIngreso();
        }
        return respuesta;
    }
    
